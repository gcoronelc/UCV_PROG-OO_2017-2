    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double capital= Double.parseDouble(this.txtCapital.getText());
        double interes= Double.parseDouble(this.txtInteres.getText());
        int meses = Integer.parseInt(this.txtNumeroMeses.getText());
        
        BancoServicio objBanco = new BancoServicio();
        
        double importe = objBanco.obtenerImporte(capital, interes, meses);
        
        importe=importe*100;
        importe=Math.round(importe);
        importe=importe/100;
        
        this.txtImporte.setText(String.valueOf(importe));
    }//GEN-LAST:event_btnCalcularActionPerformed

