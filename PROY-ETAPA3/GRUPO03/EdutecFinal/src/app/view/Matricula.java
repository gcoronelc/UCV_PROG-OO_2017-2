        List<Map<String, Object>> matriculas = controller.mostrarMatriculas(codigo);
        DefaultTableModel tabla;
        tabla = (DefaultTableModel) tblMatriculas.getModel();
        tabla.setRowCount(0);
        for (Map<String, Object> r : matriculas) {
            Object[] rowData = {
                r.get("codigoalumno"),
                r.get("alumno"),
                r.get("docente"),
                r.get("curso"),
                r.get("fechainicio"),
                r.get("fechafin"),
            };
            tabla.addRow(rowData);
        }
        limpiar();
